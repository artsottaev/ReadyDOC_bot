# Модуль для проверки юридической корректности документа

def check_document_legality(document_text: str):
    # Простая проверка: можно расширить с реальными проверками
    issues = []

    # Пример: Проверяем наличие важных юридических терминов (заглушка)
    if "необходимо" not in document_text:
        issues.append("Отсутствует обязательная юридическая фраза: 'необходимо'.")

    # Возвращаем список найденных проблем
    return issues
